public class ArithmeticOperators {

        public int additionOfTwoOperand(int firstNumber, int secondNumber) {
            return firstNumber + secondNumber;
        }

        public int subtractionOfTwoOperand(int firstNumber, int secondNumber) {

            return firstNumber - secondNumber;
        }

        public int multiplicationOfTwoOperand(int firstNumber, int secondNumber) {

            return firstNumber * secondNumber;
        }

        public int divisionOfTwoOperand(int firstNumber, int secondNumber)
        {

            return firstNumber / secondNumber;
        }
    }


